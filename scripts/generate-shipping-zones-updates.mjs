/**
 * XLSX -> SQL generator (RLS'ye takılmamak için)
 *
 * Üretim Supabase'de shipping_zones tablosunda RLS açık olduğu için anon key ile UPDATE "sessizce" etkisiz kalabiliyor.
 * Bu yüzden bu script:
 * - Excel'den şehir bazlı iskonto tablolarını okur
 * - Supabase SQL Editor'da çalıştırılacak UPDATE SQL dosyası üretir
 *
 * Üretilen dosyalar:
 * - scripts/generated/optimix_toz_discount_updates.sql
 *
 * Kullanım:
 *   cd tasyunu-front
 *   node scripts/generate-shipping-zones-updates.mjs
 */

import fs from "fs";
import path from "path";
import XLSX from "xlsx";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PROJECT_ROOT = path.resolve(__dirname, "..");

function normalize(s) {
  return String(s ?? "")
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replaceAll("ı", "i")
    .replace(/\s+/g, " ")
    .trim();
}

function parseNumberTR(val) {
  if (val == null) return null;
  const s = String(val).replace("%", "").trim();
  if (!s) return null;
  const n = Number(s.replace(",", "."));
  return Number.isFinite(n) ? n : null;
}

function findWorkbookByHint(dir, hint) {
  const files = fs.readdirSync(dir).filter((f) => f.toLowerCase().endsWith(".xlsx"));
  return files.find((f) => normalize(f).includes(normalize(hint))) || null;
}

function sheetToMatrix(workbook, sheetName) {
  const ws = workbook.Sheets[sheetName];
  if (!ws) return null;
  return XLSX.utils.sheet_to_json(ws, { header: 1, raw: false, defval: "" });
}

function parseOptimixRegionDiscount(workbook) {
  for (const sheetName of workbook.SheetNames) {
    const matrix = sheetToMatrix(workbook, sheetName);
    if (!matrix) continue;

    let headerRow = -1;
    for (let r = 0; r < matrix.length; r++) {
      const row = matrix[r].map(normalize);
      // Sağ taraftaki tabloda aynı satırda "il plaka" ve "% iskonto" var
      if (row.some((c) => c.includes("il plaka")) && row.some((c) => c.includes("iskonto"))) {
        headerRow = r;
        break;
      }
    }
    if (headerRow === -1) continue;

    const header = matrix[headerRow].map(normalize);
    const cityCodeIdx = header.findIndex((c) => c.includes("plaka"));
    if (cityCodeIdx === -1) continue;

    const iskontoIdxCandidates = header
      .map((c, idx) => ({ c, idx }))
      .filter(({ c }) => c.includes("iskonto"))
      .map(({ idx }) => idx);
    const discountIdx = iskontoIdxCandidates.find((idx) => idx > cityCodeIdx) ?? -1;
    if (discountIdx === -1) continue;

    const rows = [];
    for (let r = headerRow + 1; r < matrix.length; r++) {
      const row = matrix[r];
      const cityCode = parseInt(String(row[cityCodeIdx]).trim(), 10);
      const discount = parseNumberTR(row[discountIdx]);
      if (!Number.isFinite(cityCode) || discount == null) continue;
      rows.push({ city_code: cityCode, optimix_toz_discount: discount });
    }

    if (rows.length >= 40) return { sheetName, rows };
  }
  return null;
}

function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}

function generateSqlForOptimix(rows) {
  const lines = [];
  lines.push("-- AUTOGENERATED: Optimix bölge iskontosu -> shipping_zones.optimix_toz_discount + shipping_zones.optimix_levha_discount");
  lines.push("-- Kaynak: KALEM BAZINDA AMBALAJ TIR KAMPANYASI ...xlsx içindeki 'OPTİMİX BÖLGE İSKONTOSU' tablosu");
  lines.push("BEGIN;");
  for (const r of rows) {
    lines.push(
      `UPDATE shipping_zones SET optimix_toz_discount = ${Number(r.optimix_toz_discount).toFixed(0)}, optimix_levha_discount = ${Number(r.optimix_toz_discount).toFixed(0)} WHERE city_code = ${r.city_code};`
    );
  }
  lines.push("COMMIT;");
  lines.push("");
  lines.push("-- Kontrol:");
  lines.push("SELECT city_code, city_name, optimix_toz_discount, optimix_levha_discount FROM shipping_zones WHERE city_code IN (34,41,54,6,35) ORDER BY city_code;");
  return lines.join("\n");
}

async function main() {
  const workbookFile = findWorkbookByHint(PROJECT_ROOT, "KALEM BAZINDA AMBALAJ");
  if (!workbookFile) {
    console.error("❌ Workbook bulunamadı (hint=KALEM BAZINDA AMBALAJ).");
    process.exit(1);
  }
  const workbookPath = path.join(PROJECT_ROOT, workbookFile);
  console.log("📄 Workbook:", workbookPath);

  const workbook = XLSX.readFile(workbookPath, { cellDates: false });

  const optimix = parseOptimixRegionDiscount(workbook);
  if (!optimix) {
    console.error("❌ Optimix bölge iskontosu tablosu bulunamadı.");
    process.exit(1);
  }

  const outDir = path.join(PROJECT_ROOT, "scripts", "generated");
  ensureDir(outDir);

  const outPath = path.join(outDir, "optimix_toz_discount_updates.sql");
  fs.writeFileSync(outPath, generateSqlForOptimix(optimix.rows), "utf8");

  const ist = optimix.rows.find((r) => r.city_code === 34);
  console.log(`✅ SQL üretildi: ${outPath}`);
  console.log("🔎 Örnek (İstanbul 34):", ist);
}

main().catch((e) => {
  console.error("❌ HATA:", e);
  process.exit(1);
});


